public class WithDrawalsListBean {

    private String title;
    private String add_time_string;
    private String money_string;
    private String status;

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getAdd_time_string() {
        return add_time_string;
    }

    public void setAdd_time_string(String add_time_string) {
        this.add_time_string = add_time_string;
    }

    public String getMoney_string() {
        return money_string;
    }

    public void setMoney_string(String money_string) {
        this.money_string = money_string;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

}
